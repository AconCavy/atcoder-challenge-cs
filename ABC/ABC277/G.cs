        public static void Main()
        {
            using var sw = new StreamWriter(Console.OpenStandardOutput()) { AutoFlush = false };
            Console.SetOut(sw);
            Solve();
            Console.Out.Flush();
        }

        public static void Solve()
        {
            var (N, M, K) = Scanner.Scan<int, int, int>();
            var G = new List<int>[N].Select(x => new List<int>()).ToArray();
            for (var i = 0; i < M; i++)
            {
                var (u, v) = Scanner.Scan<int, int>();
                u--; v--;
                G[u].Add(v);
                G[v].Add(u);
            }
            var C = Scanner.ScanEnumerable<int>().ToArray();

            var invSize = new mint[N];
            for (var i = 0; i < N; i++)
            {
                invSize[i] = mint.Inverse(G[i].Count);
            }

            var dp = new mint[K + 1, N];
            dp[0, 0] = 1;
            var sum = new mint[K + 1, N];
            sum[0, 0] = 0;
            var level = new HashSet<int>[K + 1][].Select(_ => new HashSet<int>[N].Select(_ => new HashSet<int>()).ToArray()).ToArray();
            level[0][0].Add(0);

            for (var k = 0; k < K; k++)
            {
                for (var i = 0; i < N; i++)
                {
                    sum[k + 1, i] += sum[k, i];
                }

                for (var u = 0; u < N; u++)
                {
                    foreach (var v in G[u])
                    {
                        foreach (var x in level[k][u])
                        {
                            if (C[v] == 0)
                            {
                                level[k + 1][v].Add(x + 1);
                            }
                            else
                            {
                                level[k + 1][v].Add(x);
                                sum[k + 1, v] += (mint)x * x;
                            }
                        }
                    }

                    foreach (var v in G[u])
                    {
                        dp[k + 1, v] += dp[k, u] * invSize[u] * sum[k, u];
                    }
                }
            }

            mint answer = 0;
            for (var i = 0; i < N; i++)
            {
                answer += dp[K, i];
            }

            Console.WriteLine(answer);

            Printer.Print2D(sum, " ");
            Console.WriteLine();
            Printer.Print2D(dp, " ");

        }

        public static class Printer
