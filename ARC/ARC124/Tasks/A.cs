        public static void Solve()
        {
            var (N, K) = Scanner.Scan<int, int>();
            var G = new int[K + 1, N + 1];
            for (var i = 1; i <= K; i++)
            {
                for (var j = 1; j <= N; j++)
                {
                    G[i, j] = 1;
                }
            }

            for (var i = 1; i <= K; i++)
            {
                var (c, k) = Scanner.Scan<char, int>();
                G[i, k] = 2;
                if (c == 'L')
                {
                    for (var j = 1; j < k; j++)
                    {
                        G[i, j] = 0;
                    }
                }
                else
                {
                    for (var j = k + 1; j <= N; j++)
                    {
                        G[i, j] = 0;
                    }
                }
            }

            mint answer = 1;

            for (var j = 1; j <= N; j++)
            {
                mint c = 0;
                for (var k = 1; k <= K; k++)
                {
                    if (G[k, j] == 2)
                    {
                        c = 1;
                        break;
                    }
                    else
                    {
                        c += G[k, j];
                    }
                }

                answer *= c;
            }

            Console.WriteLine(answer);
        }

